A = list(input().split()) #n개의 d자리 정수
B = ['' for _ in range(len(A)+1)]
print("Before: ",A)
for d in range(1,len(A[0])+1): #자릿수
    C = [0 for _ in range(10)]
    for a in A:#원소 count
        C[int(a[-d])] += 1

    for c in range(1,len(C)):#누적합
        C[c] = C[c]+C[c-1]

    for i in range(len(A)-1,-1,-1):
        B[C[int(A[i][-d])]] = A[i]
        C[int(A[i][-d])] -= 1
        
    A=B[1:] #A갱신

print("After: ", B[1:])

